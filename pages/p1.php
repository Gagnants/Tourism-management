<h1 align="center" style="text-shadow:#0F6 2px 2px ; font-family:'Lucclassa Console', Monaco, monospace; font-size:64px; color:#0CC;"><B> City Palace</B></h1><br>
<a href="product1.php"><img src="../images/ci.jpeg"></a>
 <p>Well known as one of the biggest palaces in Rajasthan, the City Palace is a beautiful and grand building in Udaipur. Built back in 1559 by Maharana Udai Singh, the City Palace is set on the banks of Pichola Lake. The palace has a number of gateways. The main entrance of the palace is Bara Pol or the Great Gate. Another gate is the triple arched gate which is called Tripolia. There is a field next to Tripolia where elephant fights used to take place. One can find eight marble arches in the middle of these two gates.
    The beautiful City Palace features a great blend of the Mughal and Rajasthani architecture. It is set at a backdrop which offers an opportunity to have an aerial view of the entire city. The palace leaves every visitor spellbound with its domes, courtyards, corridors, rooms, pavilions, towers, and hanging gardens.</p>   

